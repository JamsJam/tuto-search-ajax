    public function chercheProduit($value): array
    {
        $conn = $this->getEntityManager()->getConnection();

        $sql = '
            SELECT p.id FROM produit as p
            JOIN categorie as c 
                ON c.id = p.categorie_id
            WHERE   p.nom LIKE :value
                OR  c.nom LIKE :value
        ';

        $stmt = $conn->prepare($sql);
        $result = $stmt->executeQuery(['value' => '%'.$value.'%']);

        return $result->fetchAllAssociative();
        
            
        
    }

